// Even Number Finder

for (let num = 0; num < 20; num++) {
    if (num % 2 === 0) {
        console.log ('Even Number:', num);
    }

    console.log(num);
}


// Odd Number Finder

for (let i = 0; i < 20; i++) {
    if (i % 2 === 1) {
         console.log ('Odd Number:', i);
    }
    console.log (i);
}


// Another Way of Even Number Finder

for (let i = 0; i <= 20; i = i + 2) {
    console.log (i);
}

// Another Way of Odd Number Finder

for (let i = 1; i <= 20; i = i + 2) {
    console.log (i);
}